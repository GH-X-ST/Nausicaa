def _generic_table(
    frame: pd.DataFrame,
    *,
    caption: str,
    label: str,
    columns: tuple[ColumnSpec, ...],
    weights: tuple[float, ...],
    rows_font: str = r"\tiny",
) -> list[str]:
    if len(columns) != len(weights):
        raise ValueError(f"Table {label} has {len(columns)} columns but {len(weights)} weights.")
    if not math.isclose(sum(weights), 16.2, rel_tol=0.0, abs_tol=0.02):
        raise ValueError(f"Table {label} column weights sum to {sum(weights):.3f}, not 16.2.")
    extrema = _column_extrema(frame, columns)
    lines = [
        r"\begin{longtblr}[",
        rf"  caption = {{{caption}}},",
        rf"  label = {{{label}}},",
        r"]{",
        rf"  rows={{font={rows_font}}},",
        r"  width=16.2cm,",
        r"  rowhead=1,",
        r"  rowsep=1pt,",
        r"  colsep=1pt,",
        r"  colspec = {",
        _generic_colspec(columns, weights),
        r"  },",
        r"}",
        r"\toprule",
        _generic_header_row(columns),
        r"\midrule",
    ]
    for _, row in frame.iterrows():
        cells = [_format_cell(row, column, extrema) for column in columns]
        lines.append("\n  " + "\n  & ".join(cells) + r" \\")
    lines.extend([r"\bottomrule", r"\end{longtblr}"])
    return lines


def _generic_colspec(columns: tuple[ColumnSpec, ...], weights: tuple[float, ...]) -> str:
    specs = []
    for column, weight in zip(columns, weights):
        align = "l" if column.fmt == "text" or column.raw_latex else "c"
        specs.append(f"    X[{weight:.2f},{align},m]")
    return "\n".join(specs)


def _generic_header_row(columns: tuple[ColumnSpec, ...]) -> str:
    headers = [rf"\SetCell{{m}} \tiny\textbf{{{column.header}}}" for column in columns]
    return "\n  " + "\n  & ".join(headers) + r" \\"


def _column_extrema(frame: pd.DataFrame, columns: tuple[ColumnSpec, ...]) -> dict[str, tuple[float, float]]:
    extrema: dict[str, tuple[float, float]] = {}
    for column in columns:
        if column.higher_is_better is None:
            continue
        values = pd.to_numeric(frame.get(column.key, pd.Series(dtype=float)), errors="coerce").dropna()
        if not values.empty:
            extrema[column.key] = (float(values.min()), float(values.max()))
    return extrema


def _speed_colspec() -> str:
    alignments = ["l", "l", "c", "c"] + ["c"] * len(SPEED_METRICS)
    return "\n".join(f"    X[{weight:.2f},{align},m]" for weight, align in zip(SPEED_TABLE_WEIGHTS, alignments))


def _speed_header_row() -> str:
    headers = [
        r"\SetCell{m} \tiny\textbf{$v_0$}",
        r"\SetCell{m} \tiny\textbf{Cluster}",
        r"\SetCell{m} \tiny\textbf{$h$}",
        r"\SetCell{m} \tiny\textbf{$n$}",
    ]
    headers.extend(rf"\SetCell{{m}} \tiny\textbf{{{column.header}}}" for column in SPEED_METRICS)
    return "\n  " + "\n  & ".join(headers) + r" \\"


def _speed_metric_extrema(frame: pd.DataFrame) -> dict[str, tuple[float, float]]:
    extrema: dict[str, tuple[float, float]] = {}
    for metric in SPEED_METRICS:
        values = pd.to_numeric(frame.get(metric.source, pd.Series(dtype=float)), errors="coerce").dropna()
        if not values.empty:
            extrema[metric.source] = (float(values.min()), float(values.max()))
    return extrema


def _format_speed_metric(row: pd.Series, metric: SpeedMetric, extrema: dict[str, tuple[float, float]]) -> str:
    value = row.get(metric.source, math.nan)
    number = _finite_float(value)
    if number is None:
        return "--"
    text = _format_count_rate(row, rate=number, count_key=metric.count_source, denom_key="launch_count") if metric.fmt == "pct" and metric.count_source else _format_number(number, metric.fmt)
    limits = extrema.get(metric.source)
    if limits is None or math.isclose(limits[0], limits[1], rel_tol=0.0, abs_tol=1e-12):
        return text
    best = limits[1] if metric.higher_is_better else limits[0]
    worst = limits[0] if metric.higher_is_better else limits[1]
    if _format_number(best, metric.fmt) == _format_number(worst, metric.fmt):
        return text
    return _colour_if_extreme(number, text, best, worst)


def _format_cell(row: pd.Series, column: ColumnSpec, extrema: dict[str, tuple[float, float]]) -> str:
    value = row.get(column.key, math.nan)
    if column.fmt == "text":
        if _is_missing(value):
            return "--"
        text = str(value) if column.raw_latex else latex_escape(str(value))
        return text
    number = _finite_float(value)
    if number is None:
        return "--"
    if column.fmt == "pct" and column.count_key and column.denom_key:
        text = _format_count_rate(row, rate=number, count_key=column.count_key, denom_key=column.denom_key)
    else:
        text = _format_number(number, column.fmt)
    limits = extrema.get(column.key)
    if limits is None or column.higher_is_better is None or math.isclose(limits[0], limits[1], rel_tol=0.0, abs_tol=1e-12):
        return text
    best = limits[1] if column.higher_is_better else limits[0]
    worst = limits[0] if column.higher_is_better else limits[1]
    if _format_number(best, column.fmt) == _format_number(worst, column.fmt):
        return text
    return _colour_if_extreme(number, text, best, worst)


def _format_count_rate(row: pd.Series, *, rate: float, count_key: str | None, denom_key: str | None) -> str:
    count = _finite_float(row.get(count_key, math.nan)) if count_key else None
    total = _finite_float(row.get(denom_key, math.nan)) if denom_key else None
    if count is None or total is None:
        return _format_number(rate, "pct")
    return f"{int(round(count))}/{int(round(total))} ({100.0 * rate:.1f})"


def _format_number(number: float, fmt: str) -> str:
    if fmt == "int":
        return str(int(round(number)))
    if fmt == "pct":
        return f"{100.0 * number:.1f}"
    if fmt == "float1":
        return f"{number:.1f}"
    if fmt == "float2":
        return f"{number:.2f}"
    if fmt == "float3":
        return f"{number:.3f}"
    if fmt == "score":
        return f"{number:.1f}"
    if fmt in {"energy", "time"}:
        return f"{number:.2f}"
    return f"{number:.2f}"


def _colour_if_extreme(number: float, text: str, best: float, worst: float) -> str:
    if math.isclose(number, best, rel_tol=0.0, abs_tol=1e-12):
        return rf"\textcolor{{ICLBlue}}{{{text}}}"
    if math.isclose(number, worst, rel_tol=0.0, abs_tol=1e-12):
        return rf"\textcolor{{ICLRed}}{{{text}}}"
    return text


def _format_count(value: object) -> str:
    number = _finite_float(value)
    return "--" if number is None else str(int(round(number)))


def _safe_div(numer: object, denom: object) -> pd.Series | float:
    numer_series = pd.to_numeric(numer, errors="coerce")
    denom_series = pd.to_numeric(denom, errors="coerce")
    result = numer_series / denom_series
    if isinstance(result, pd.Series):
        return result.replace([float("inf"), float("-inf")], pd.NA)
    return result if math.isfinite(float(result)) else math.nan


def _finite_float(value: object) -> float | None:
    try:
        number = float(value)
    except (TypeError, ValueError):
        return None
    return number if math.isfinite(number) else None



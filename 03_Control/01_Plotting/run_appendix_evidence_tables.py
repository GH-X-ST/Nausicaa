def _r8_tables(r8_root: Path, outcome_root: Path) -> list[str]:
    library = _read_csv(r8_root / "metrics" / "library_size_case_summary.csv")
    speed = _read_csv(r8_root / "metrics" / "speed_bin_coverage_audit.csv")
    medoid = _read_csv(r8_root / "metrics" / "coverage_medoid_selection_audit.csv")
    outcome = _read_csv(outcome_root / "metrics" / "outcome_model_summary.csv")

    baseline_count = float(
        library.loc[library["library_size_case_id"] == "no_cluster_no_merge", "representative_count"].iloc[0]
    )
    speed_summary = (
        speed.groupby("library_size_case_id", dropna=False)
        .agg(
            speed_audit_rows=("speed_bin_coverage_status", "count"),
            speed_coverage_passed=("speed_bin_coverage_status", lambda s: int((s.astype(str) == "passed").sum())),
            selected_speed_bin_count=("selected_speed_bin_count", "mean"),
            source_speed_bin_count=("source_speed_bin_count", "mean"),
        )
        .reset_index()
    )
    speed_summary["speed_coverage_pass_rate"] = _safe_div(
        speed_summary["speed_coverage_passed"], speed_summary["speed_audit_rows"]
    )
    outcome_summary = (
        outcome.groupby("library_size_case_id", dropna=False)
        .agg(
            outcome_rows=("compact_library_id", "count"),
            primitive_family_count=("primitive_id", "nunique"),
            transition_entry_class_count=("transition_entry_class", "nunique"),
            transition_success_probability=("transition_success_probability", "mean"),
            continuation_probability=("continuation_probability", "mean"),
            hard_failure_risk=("hard_failure_risk", "mean"),
            expected_energy_residual_m=("expected_energy_residual_m", "mean"),
            expected_lift_dwell_time_s=("expected_lift_dwell_time_s", "mean"),
        )
        .reset_index()
    )
    case_summary = library.merge(speed_summary, on="library_size_case_id", how="left").merge(
        outcome_summary, on="library_size_case_id", how="left"
    )
    case_summary["library_label"] = case_summary["library_size_case_id"].map(LIBRARY_LABELS)
    case_summary["compression_ratio"] = baseline_count / case_summary["representative_count"]
    case_summary = _order_by_case(case_summary)

    entry_summary = (
        medoid.groupby(["library_size_case_id", "transition_entry_class"], dropna=False)
        .agg(
            representative_count=("transition_object_id", "count"),
            primitive_family_count=("primitive_id", "nunique"),
            speed_bin_count=("local_lqr_speed_bin_id", "nunique"),
            mean_reference_speed_m_s=("local_lqr_reference_speed_m_s", "mean"),
            mean_coverage_gain=("coverage_marginal_gain", "mean"),
            mean_medoid_quality=("medoid_quality_score", "mean"),
            mean_distance_to_center=("medoid_distance_to_group_center", "mean"),
        )
        .reset_index()
    )
    entry_summary["library_label"] = entry_summary["library_size_case_id"].map(LIBRARY_LABELS)
    entry_summary["entry_label"] = entry_summary["transition_entry_class"].map(_human_label)
    entry_summary = _order_by_case(entry_summary).sort_values(["case_order", "transition_entry_class"])

    lines = [
        r"\subsection{Library Compression and Outcome-Model Tables}",
        "",
        r"\noindent\footnotesize",
        r"These tables report how the R7 survival set is compressed into online primitive-library tiers.",
        r"Compression is coverage-aware: the medoid selection keeps primitive-family, transition-entry-class, and local speed-bin evidence instead of selecting only the highest nominal score.",
        r"Count-backed coverage rates are reported as count/$n$ (percentage); model probabilities without a direct per-table denominator remain reported as percentages.",
        r"The outcome model remains simulation-only evidence used by the outer-loop governor; it does not mutate the frozen primitive controllers.",
        "",
    ]
    lines.extend(
        _generic_table(
            case_summary,
            caption=(
                "R8 library-size tiers and outcome-model summary. "
                "The no-cluster tier is the offline reference, while heavy and balanced are the primary real-flight candidate tiers."
            ),
            label="tab:appendix-r8-library-tier-summary",
            columns=(
                ColumnSpec("library_label", "Library tier"),
                ColumnSpec("representative_count", "Reps.", "int", False),
                ColumnSpec("compression_ratio", "Comp.", "float1", True),
                ColumnSpec("primitive_family_count", "Families", "int", True),
                ColumnSpec("transition_entry_class_count", "Entries", "int", True),
                ColumnSpec("speed_coverage_pass_rate", "Speed cov.", "pct", True, count_key="speed_coverage_passed", denom_key="speed_audit_rows"),
                ColumnSpec("selected_speed_bin_count", "Sel. speeds", "float1", True),
                ColumnSpec("transition_success_probability", "Success", "pct", True),
                ColumnSpec("continuation_probability", "Cont.", "pct", True),
                ColumnSpec("hard_failure_risk", "Hard risk", "pct", False),
                ColumnSpec("expected_energy_residual_m", r"$\Delta E$", "energy", True),
                ColumnSpec("expected_lift_dwell_time_s", r"$t_L$", "time", True),
            ),
            weights=(4.30, 1.00, 1.00, 1.00, 1.00, 1.25, 1.25, 1.15, 1.10, 1.15, 1.00, 1.00),
        )
    )
    lines.append("")
    lines.extend(
        _generic_table(
            entry_summary,
            caption=(
                "R8 coverage-aware medoid selection by transition-entry class. "
                "The table shows how each compression tier preserves entry-condition and speed-bin coverage for the receding-horizon selector."
            ),
            label="tab:appendix-r8-entry-medoid-coverage",
            columns=(
                ColumnSpec("library_label", "Library tier"),
                ColumnSpec("entry_label", "Entry class"),
                ColumnSpec("representative_count", "Reps.", "int", True),
                ColumnSpec("primitive_family_count", "Families", "int", True),
                ColumnSpec("speed_bin_count", "Speeds", "int", True),
                ColumnSpec("mean_reference_speed_m_s", r"$\bar{v}$", "float1", True),
                ColumnSpec("mean_coverage_gain", "Cov. gain", "float3", True),
                ColumnSpec("mean_medoid_quality", "Quality", "float3", True),
                ColumnSpec("mean_distance_to_center", "Dist.", "float2", False),
            ),
            weights=(2.10, 2.40, 1.10, 1.10, 1.10, 1.10, 1.95, 2.00, 3.35),
            rows_font=r"\tiny",
        )
    )
    return lines



def _speed_cluster_policy_table(frame: pd.DataFrame, *, stage: str, caption: str, label: str) -> list[str]:
    if frame.empty:
        return []
    ordered = frame.copy()
    ordered["launch_speed_bin_id"] = pd.Categorical(ordered["launch_speed_bin_id"], SPEED_ORDER, ordered=True)
    ordered["library_size_case_id"] = pd.Categorical(ordered["library_size_case_id"], LIBRARY_ORDER, ordered=True)
    ordered["policy_id"] = pd.Categorical(ordered["policy_id"], POLICY_ORDER, ordered=True)
    ordered = ordered.sort_values(["launch_speed_bin_id", "library_size_case_id", "policy_id"])
    extrema = _speed_metric_extrema(ordered)
    lines = [
        r"\begin{longtblr}[",
        rf"  caption = {{{caption}}},",
        rf"  label = {{{label}}},",
        r"]{",
        r"  rows={font=\tiny},",
        r"  width=16.2cm,",
        r"  rowhead=1,",
        r"  rowsep=1pt,",
        r"  colsep=1pt,",
        r"  colspec = {",
        _speed_colspec(),
        r"  },",
        r"}",
        r"\toprule",
        _speed_header_row(),
        r"\midrule",
    ]
    row_counter = 0
    for speed_id in SPEED_ORDER:
        speed_frame = ordered[ordered["launch_speed_bin_id"].astype(str) == speed_id]
        if speed_frame.empty:
            continue
        if row_counter:
            lines.append(r"\midrule")
        speed_started = False
        for library_id in LIBRARY_ORDER:
            library_frame = speed_frame[speed_frame["library_size_case_id"].astype(str) == library_id]
            if library_frame.empty:
                continue
            library_started = False
            for policy_id in POLICY_ORDER:
                row = library_frame[library_frame["policy_id"].astype(str) == policy_id]
                if row.empty:
                    continue
                cells = []
                cells.append(rf"\SetCell[r=20]{{l,m}} {SPEED_LABELS[speed_id]}" if not speed_started else "")
                cells.append(rf"\SetCell[r=4]{{l,m}} {LIBRARY_LABELS[library_id]}" if not library_started else "")
                cells.append(POLICY_LABELS[policy_id])
                cells.append(_format_count(row.iloc[0].get("launch_count", "")))
                for metric in SPEED_METRICS:
                    cells.append(_format_speed_metric(row.iloc[0], metric, extrema))
                lines.append("\n  " + "\n  & ".join(cells) + r" \\")
                speed_started = True
                library_started = True
                row_counter += 1
    lines.extend([r"\bottomrule", r"\end{longtblr}"])
    return lines



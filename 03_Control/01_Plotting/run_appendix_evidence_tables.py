def _compact_tau_string(value: object) -> str:
    text = str(value)
    try:
        parsed = json.loads(text)
    except json.JSONDecodeError:
        return text
    if not isinstance(parsed, list) or not parsed:
        return text
    rounded = [round(float(item), 3) for item in parsed]
    if len(set(rounded)) == 1:
        return f"{rounded[0]:.3f} s on all aggregate surfaces"
    return ", ".join(f"{item:.3f}" for item in rounded) + " s"


